#include "libft.h"

int		ft_atoi(const char *str)
{
	int			i;
	long long	result;
	int			sign;

	i = 0;
	result = 0;
	sign = 1;
	while (ft_iswspace(str[i]))
		i++;
	if ((str[i] == '-') || (str[i] == '+'))
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = (result * 10) + (sign * (str[i] - '0'));
		i++;
		if (result > 2147483648)
			return (-1);
		if (result < -2147483648)
			return (0);
	}
	return (result);
}
