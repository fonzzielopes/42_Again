#include <stdio.h>

int		ft_str_is_alpha(char *str)
{
	int i;

	i = 1;
	while (*str != '\0')
	{
		if ((*str >= 'a' && *str <= 'z') || (*str >= 'A' && *str <= 'Z'))
		{
			i = 1;
		}
		else
			return (0);
		str++;
	}
	return (i);
}
