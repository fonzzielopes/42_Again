#include <stdio.h>

int		ft_str_is_numeric(char *str)
{
	int i;

	i = 1;
	while (*str != '\0')
	{
		if (*str >= '0' && *str <= '9')
		{
			i = 1;
		}
		else
			return (0);
		str++;
	}
	return (i);
}
